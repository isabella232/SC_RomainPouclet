import Foundation
import UIKit

struct Profile {

  /// The Name of the Social Network
  let name: String

  /// A quick explaination of why you'd use thie social network
  let content: String

  /// The URL to the profile on this Social Network
  let url: URL

  /// An icon for this social network
  let icon: UIImage

  /// A color associated to this social network
  let tint: UIColor
}
